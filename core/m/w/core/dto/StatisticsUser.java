package m.w.core.dto;

import java.io.Serializable;
/**
 * 用于统计传递数据使用
 * @author admin
 *
 */
public class StatisticsUser implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 2631660898056117236L;

	//名称
	public String name;
	
	//该名称的人数
	public int number;
	
	//该名称所占比例
	public String ratio;
	
	public StatisticsUser(String name,int number,String ratio){
		this.name=name;
		this.number=number;
		this.ratio=ratio;
	}
	
	public StatisticsUser(){
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public String getRatio() {
		return ratio;
	}

	public void setRatio(String ratio) {
		this.ratio = ratio;
	}
	
	

}
