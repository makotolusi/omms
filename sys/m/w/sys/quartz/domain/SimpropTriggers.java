package m.w.sys.quartz.domain;

import java.io.Serializable;

import org.nutz.dao.entity.annotation.ColDefine;
import org.nutz.dao.entity.annotation.Column;
import org.nutz.dao.entity.annotation.PK;
import org.nutz.dao.entity.annotation.Table;

@Table("QRTZ_SIMPROP_TRIGGERS")
@PK({ "schedName", "triggerName", "triggerGroup" })
public class SimpropTriggers implements Serializable {
    private static final long serialVersionUID = 2507708333141569158L;


    @Column("SCHED_NAME")
	@ColDefine(notNull = true, width = 120)
	private String schedName;


	@Column("TRIGGER_NAME")
	@ColDefine(notNull = true, width = 200)
	private String triggerName;


	@Column("TRIGGER_GROUP")
	@ColDefine(notNull = true, width = 200)
	private String triggerGroup;


	@Column("STR_PROP_1")
	@ColDefine(width = 512)
	private String strProp1;


	@Column("STR_PROP_2")
	@ColDefine(width = 512)
	private String strProp2;


	@Column("STR_PROP_3")
	@ColDefine(width = 512)
	private String strProp3;


	@Column("INT_PROP_1")
	private Integer intProp1;


	@Column("INT_PROP_2")
	private Integer intProp2;


	@Column("LONG_PROP_1")
	private Long longProp1;


	@Column("LONG_PROP_2")
	private Long longProp2;


	@Column("DEC_PROP_1")
	@ColDefine(width = 13, precision=4)
	private Double decProp1;


	@Column("DEC_PROP_2")
	@ColDefine(width = 13, precision=4)
	private Double decProp2;


	@Column("BOOL_PROP_1")
	@ColDefine(width = 1)
	private Boolean boolProp1;


	@Column("BOOL_PROP_2")
	@ColDefine(width = 1)
	private Boolean boolProp2;


    public String getSchedName() {
        return schedName;
    }


    public void setSchedName(String schedName) {
        this.schedName = schedName;
    }


    public String getTriggerName() {
        return triggerName;
    }


    public void setTriggerName(String triggerName) {
        this.triggerName = triggerName;
    }


    public String getTriggerGroup() {
        return triggerGroup;
    }


    public void setTriggerGroup(String triggerGroup) {
        this.triggerGroup = triggerGroup;
    }


    public String getStrProp1() {
        return strProp1;
    }


    public void setStrProp1(String strProp1) {
        this.strProp1 = strProp1;
    }


    public String getStrProp2() {
        return strProp2;
    }


    public void setStrProp2(String strProp2) {
        this.strProp2 = strProp2;
    }


    public String getStrProp3() {
        return strProp3;
    }


    public void setStrProp3(String strProp3) {
        this.strProp3 = strProp3;
    }


    public Integer getIntProp1() {
        return intProp1;
    }


    public void setIntProp1(Integer intProp1) {
        this.intProp1 = intProp1;
    }


    public Integer getIntProp2() {
        return intProp2;
    }


    public void setIntProp2(Integer intProp2) {
        this.intProp2 = intProp2;
    }


    public Long getLongProp1() {
        return longProp1;
    }


    public void setLongProp1(Long longProp1) {
        this.longProp1 = longProp1;
    }


    public Long getLongProp2() {
        return longProp2;
    }


    public void setLongProp2(Long longProp2) {
        this.longProp2 = longProp2;
    }


    public Double getDecProp1() {
        return decProp1;
    }


    public void setDecProp1(Double decProp1) {
        this.decProp1 = decProp1;
    }


    public Double getDecProp2() {
        return decProp2;
    }


    public void setDecProp2(Double decProp2) {
        this.decProp2 = decProp2;
    }


    public Boolean getBoolProp1() {
        return boolProp1;
    }


    public void setBoolProp1(Boolean boolProp1) {
        this.boolProp1 = boolProp1;
    }


    public Boolean getBoolProp2() {
        return boolProp2;
    }


    public void setBoolProp2(Boolean boolProp2) {
        this.boolProp2 = boolProp2;
    }

}