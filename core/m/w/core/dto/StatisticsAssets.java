package m.w.core.dto;

import java.io.Serializable;

/**
 * 用于统计资产使用
 * @author Administrator
 *
 */

public class StatisticsAssets implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	//部门名称
	public String name;
	//资产类型
	public String pname;
	//使用数量
	public int number;
	
		
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public int getNumber() {
		return number;
	}
	
	public StatisticsAssets(String name, String pname, int number) {
		super();
		this.name = name;
		this.pname = pname;
		this.number = number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	
	
		
		
		
}
