package m.w.chart.fcf.stacked;

import m.w.chart.fcf.Chartable;

public class StackedColumn3D extends StackedChart<StackedColumn3D> implements Chartable {
    public StackedColumn3D canvasBaseColor(String canvasBaseColor){
        attr.put("canvasBaseColor", canvasBaseColor);
        return this;
    }

    public StackedColumn3D canvasBaseDepth(Integer canvasBaseDepth){
        attr.put("canvasBaseDepth", canvasBaseDepth);
        return this;
    }

    public StackedColumn3D canvasBgDepth(Integer canvasBgDepth){
        attr.put("canvasBgDepth", canvasBgDepth);
        return this;
    }

    public StackedColumn3D rotateNames(Boolean rotateNames){
        attr.put("rotateNames", rotateNames);
        return this;
    }

    public StackedColumn3D showCanvasBase(Boolean showCanvasBase){
        attr.put("showCanvasBase", showCanvasBase);
        return this;
    }

    public StackedColumn3D showCanvasBg(Boolean showCanvasBg){
        attr.put("showCanvasBg", showCanvasBg);
        return this;
    }

    public StackedColumn3D zeroPlaneBorderColor(String zeroPlaneBorderColor){
        attr.put("zeroPlaneBorderColor", zeroPlaneBorderColor);
        return this;
    }

    public StackedColumn3D zeroPlaneShowBorder(Boolean zeroPlaneShowBorder){
        attr.put("zeroPlaneShowBorder", zeroPlaneShowBorder);
        return this;
    }
}
