package m.w.chart.fcf.ss;

import m.w.chart.fcf.Chartable;

public class Pie3D extends SingleSeriesChart<Pie3D> implements Chartable {
    public Pie3D nameTBDistance(Integer nameTBDistance){
        attr.put("nameTBDistance", nameTBDistance);
        return this;
    }

    public Pie3D pieBorderAlpha(Integer pieBorderAlpha){
        attr.put("pieBorderAlpha", pieBorderAlpha);
        return this;
    }

    public Pie3D pieBorderThickness(Integer pieBorderThickness){
        attr.put("pieBorderThickness", pieBorderThickness);
        return this;
    }

    public Pie3D pieFillAlpha(Integer pieFillAlpha){
        attr.put("pieFillAlpha", pieFillAlpha);
        return this;
    }

    public Pie3D pieRadius(Number pieRadius){
        attr.put("pieRadius", pieRadius);
        return this;
    }

    public Pie3D pieSliceDepth(Integer pieSliceDepth){
        attr.put("pieSliceDepth", pieSliceDepth);
        return this;
    }

    public Pie3D pieYScale(Integer pieYScale){
        attr.put("pieYScale", pieYScale);
        return this;
    }

    public Pie3D showPercentageInLabel(Boolean showPercentageInLabel){
        attr.put("showPercentageInLabel", showPercentageInLabel);
        return this;
    }

    public Pie3D showPercentageValues(Boolean showPercentageValues){
        attr.put("showPercentageValues", showPercentageValues);
        return this;
    }

    public Pie3D showValues(Boolean showValues){
        attr.put("showValues", showValues);
        return this;
    }

}
